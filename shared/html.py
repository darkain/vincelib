import	re




################################################################################
# LOCAL IMPORTS
################################################################################
from	.	import	file




################################################################################
# PARSE OUT THE CONTENTS OF A WEB DIRECTORY
################################################################################
def webdir(url):
	data	= re.findall('href=".*"', file.read(url))
	out		= []

	for item in data:
		item = item[6:-1]
		
		if item in ['./', '../']: continue

		out.append(item)

	return out

